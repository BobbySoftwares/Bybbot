from typing import Type


class Savage:
    """Random variant of the power. Default."""

    sub_power: Type


class Targetted:
    """Targetted variant of the power. Default."""

    sub_power: Type


class Blind:
    """Random targetted variant of the power."""

    sub_power: Type


class Measured:
    """Constant variant of the power."""

    sub_power: Type
